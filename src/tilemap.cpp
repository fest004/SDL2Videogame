tilemap::tilemap() {
  textureMissing = textureManager::loadTexture(
      "../assets/Tilemaps/CityTilemap/Tiles/IndustrialTile_60.png");
  ground1 = textureManager::loadTexture(
      "../assets/Tilemaps/CityTilemap/Tiles/IndustrialTile_05.png");
  platform2 = textureManager::loadTexture(
      "../assets/Tilemaps/CityTilemap/Tiles/IndustrialTile_18.png");

  LoadTilemap(defaultMap);

  source.x = 0;
  source.y = 0;
  source.h = 32;
  source.w = 32;
  destination.h = 32;
  destination.w = 32;

  destination.x = 0;
  destination.y = 0;
}

void tilemap::LoadTilemap(int array[20][25]) {
  for (int row = 0; row < 20; row++) {
    for (int column = 0; column < 25; column++) {
      map[row][column] = array[row][column];
    }
  }
}

void tilemap::DrawTileMap() {
  int type = 0;

  for (int row = 0; row < 20; row++) {
    for (int column = 0; column < 25; column++) {
      type = map[row][column];

      destination.x = column * 32;
      destination.y = row * 32;

      switch (type) {
      case 0:
        textureManager::DrawTexture(NULL, source, destination);
        break;

      case 1:
        textureManager::DrawTexture(ground1, source, destination);
        break;

      case 2:
        textureManager::DrawTexture(platform2, source, destination);
        break;

      default:
        textureManager::DrawTexture(textureMissing, source, destination);
        break;
      }
    }
  }
}
